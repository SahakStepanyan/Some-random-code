package Week3_Day2;

public class Fixing {
    public static void main(String[] args) {
        System.out.println(get(7,0.2f));
        System.out.println(get(9.9f, 7));
    }
    public static float get(int i, float b){
        float a = i;
        return a;
    }
    public static int get(float i, int b){
        b += 7;
        return b;
    }
}
